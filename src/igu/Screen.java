    private void BtnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnNextActionPerformed
        String StringIndex = ShowIndex.getText();
        int IntIndex = Integer.parseInt(StringIndex);
        if(IntIndex < 16){
            IntIndex++;
            StringIndex = String.valueOf(IntIndex);
            ShowIndex.setText(StringIndex);
            InputName.setText(name[IntIndex]);
            InputSurname.setText(surname[IntIndex]);
            InputDocument.setText(document[IntIndex]);
            InputBorn.setText(born[IntIndex]);
            InputPhone.setText(phone[IntIndex]);
            InputHome.setText(home[IntIndex]);
        }
    }//GEN-LAST:event_BtnNextActionPerformed

