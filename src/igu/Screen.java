    private void BtnPreviosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPreviosActionPerformed
        String StringIndex = ShowIndex.getText();
        int IntIndex = Integer.parseInt(StringIndex);
        if(IntIndex > 0){
            IntIndex--;
            StringIndex = String.valueOf(IntIndex);
            ShowIndex.setText(StringIndex);
            InputName.setText(name[IntIndex]);
            InputSurname.setText(surname[IntIndex]);
            InputDocument.setText(document[IntIndex]);
            InputBorn.setText(born[IntIndex]);
            InputPhone.setText(phone[IntIndex]);
            InputHome.setText(home[IntIndex]);
        }
    }//GEN-LAST:event_BtnPreviosActionPerformed

    private void BtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveActionPerformed
        String strindex = ShowIndex.getText();
        int index = Integer.parseInt(strindex);
        name[index] = InputName.getText();
        surname[index] = InputSurname.getText();
        document[index] = InputDocument.getText();
        born[index] = InputBorn.getText();
        phone[index] = InputPhone.getText();
        home[index] = InputHome.getText();
    }//GEN-LAST:event_BtnSaveActionPerformed


